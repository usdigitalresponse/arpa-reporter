<template>
  <div>
    <h2>Users</h2>
    <div class="mb-4">
      <router-link to="/new_user" class="btn btn-primary"
        >Create New User</router-link
      >
    </div>
    <table class="table table-striped">
      <thead>
        <tr>
          <th>Email</th>
          <th>Name</th>
          <th>Role</th>
          <th>Agency</th>
          <th>Created At</th>
          <th></th>
        </tr>
      </thead>
      <tbody>
        <tr :key="user.email" v-for="user in users">
          <td>{{ user.email }}</td>
          <td>{{ user.name }}</td>
          <td>{{ user.role }}</td>
          <td>{{ agencyName(user.agency_id) }}</td>
          <td>{{ user.created_at }}</td>
          <td><router-link :to="`/users/${user.id}`">Edit</router-link></td>
        </tr>
      </tbody>
    </table>
  </div>
</template>

<script>
export default {
  name: 'Users',
  computed: {
    users: function () {
      const { users } = this.$store.state.users.configuration
      return users
    }
  },
  methods: {
    agencyName (id) {
      return this.$store.getters.agencyName(id)
    }
  }
}
</script>
